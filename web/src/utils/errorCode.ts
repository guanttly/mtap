/**
 * 根据错误码获取用户友好提示
 * 对于参数校验错误 (1004)，附加后端 detail 作为补充
 */
export function getErrorMessage(code: number, detail?: string): string {
  const msg = errorMessages[code]
  if (!msg) {
    return detail || '未知错误'
  }
  // 1004 参数错误时，拼接具体字段信息帮助用户定位
  if (code === 1004 && detail) {
    return `${msg}：${detail}`
  }
  return msg
}
